let grade = "B";

switch (grade) {
  case "A":
    console.log("Xuất sắc!");
    break;
  case "B":
    console.log("Giỏi!");
    break;
  case "C":
    console.log("Khá!");
    break;
  case "D":
    console.log("Trung bình!");
    break;
  case "F":
    console.log("Rớt môn!");
    break;
  default:
    console.log("Điểm không hợp lệ!");
}
